public class doWhileLoops{
    public doWhileLoops(){
        /*
        do while loops are similar to while loops,
        do..while loop is guaranteed to excecute atleast once.
         */

    }

    public static void main(String[] args){
        int x = 1;
        do{
            System.out.println(x);
            x++;
        }while(x>10);
    }
}
